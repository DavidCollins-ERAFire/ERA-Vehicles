import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva } from "class-variance-authority";
import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-burgundy-700 text-white shadow hover:bg-burgundy-800",
        gold: "bg-gold-400 text-burgundy-900 font-semibold shadow hover:bg-gold-300",
        outline:
          "border border-burgundy-200 bg-white text-burgundy-800 hover:bg-burgundy-50",
        secondary: "bg-burgundy-50 text-burgundy-800 hover:bg-burgundy-100",
        ghost: "hover:bg-burgundy-50 text-burgundy-800",
        destructive: "bg-red-600 text-white shadow hover:bg-red-700",
        link: "text-burgundy-700 underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3 text-xs",
        lg: "h-11 rounded-md px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: { variant: "default", size: "default" },
  }
);

const Button = React.forwardRef(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    );
  }
);
Button.displayName = "Button";

export { Button, buttonVariants };
